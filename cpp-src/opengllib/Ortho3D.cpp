//-----------------------------------------------------------------------------
void Ortho3D::apply ()
{
	glOrtho( m_Left,
			 m_Right,
			 m_Bottom,
			 m_Top,
			 m_Near,
			 m_Far);
}
//-----------------------------------------------------------------------------
void Ortho3D::reshape (int w, int h)
{
	if(h==0) h =1;
	m_Left = -w;
	m_Right = w;
	m_Bottom = -h;
	m_Top = h;
}
