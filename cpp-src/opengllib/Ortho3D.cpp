bool Ortho3D::saveToFile (fstream& out, int noTabs)
{
	string tabs = "";
	if(0 != noTabs)
	for(int i=0; i<noTabs; i++)
	{
		tabs.append("  ");
	}
	out << tabs << "Ortho3d save to file" << endl;
	return true;
}
